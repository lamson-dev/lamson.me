    <h1 class="post-title">Usability Testing Day – One Good Experience</h1>

    <div class="post-content">
        <p>It's finally that day, the day our guild matching website got to be tested by users. Our
            team reserved two rooms in the CULC from 12:00 to 3:00pm, each for one and a half hour. A dozen Dunkin
            donuts
            were the incentive for the testers! We ended our usability testing days with 3 testers and some very good
            feedbacks.
        </p>

        <p>

            At first, we spent an hour waiting for our first tester to come. He ended up not showing up. We decided to
            go
            around the CULC to look for 'similar faces' - our friends to ask them be our testers, but no luck. One of
            our
            team member asked his roommate to be a tester for our site and it turned out well. Later, two other students
            came and tested our website. What we should have done is to ask for the testers' confirmation prior to the
            testing time, maybe a couple hours before just to make sure that they would come.
        </p>

        <p>
            I remember a day last year when I was a usability tester for a user interface of a copy machine, the
            experience
            is very similar, except I was on the opposite side. When I was doing the tasks given to me that time, I
            struggled not knowing how to do it because of the unclear interface. I tried to ask questions so that I
            could
            complete the tasks but I didn't get any response. I felt disappointed because I couldn't complete the tasks.
        </p>

        <p>
            This time, being on the opposite side, I understand why I couldn't give instructions to the testers. The
            main
            point is to see how the users interact with the website and figure out what to do to complete the assigned
            tasks. If they couldn't complete the tasks, it's a failure of our team didn't have a good design for the
            website.
        </p>

        <p>
            As I was recording video of the testers' interactions, I paid close attention to how the testers navigate
            through our websites. It's interesting to see the testers did things that we wouldn't think that they would
            do.
            Even though we thought we gave the testers clear instructions, it's not that clear if we're the user. I've
            learned that it's very important to put ourselves in users' position to test our website so that we could
            avoid
            allowing users to do unexpected thing on the site.
        </p>

        <p>
            Overall, I think our team usability testing day was successful. If I we had a second testing day, I would
            try to
            find a female testers and have more than three testers so we can have diversity in our data.
        </p>
    </div>
</div>

