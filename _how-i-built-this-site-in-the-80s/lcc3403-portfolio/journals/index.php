            telling me that I have new skills/spells for both my character and its pet, and one of the popups asked me
            to choose a specialization for my character. I carefully read all the Hunter specialization descriptions,
            and looked up for more information on WoW wiki.
        </p>

        <p>

        </p>

        <p>
            I decided to choose Marksmanship for my characters for the following reasons:
        </p>
        <ul>
            <li>- I like to cause bigger damage to a mob on a single shot to kill them faster</li>
            <li>- I like to use a variety of skills/spells onto a mob</li>
            <li>- I don't like to control more than one thing at a time, which I assume I need to control my character
                as
                well as its pets if I chose Beast Mastery
            </li>
            <li>- Survival spec seems very vague to me... does it means I need to set up traps and lure mobs to it?</li>
            <li>- Also, since I most likely will not play WoW again after LCC 3403, so I don't think I'll reach levels
                25.My character will have more skills from now until level 20 if it specializes in Marksmanship.
            </li>
        </ul>
