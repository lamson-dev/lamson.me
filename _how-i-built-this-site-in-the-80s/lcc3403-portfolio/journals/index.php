            everywhere on your feet, and yes, it's very slow. Later in the game, a quest requires me to travel to a
            different area on the map. I discovered that I can talk to a flight master and ask for a dragonhawk. It was
            so cool. I did not expect there are transportation services like this in the game. I thought the only way I
            could travel faster is to use some sort of portals (like the ones in the games I used to play), or get a
            mount.</p>

        <p>

            Later in the week when I needed to join the class to go into the dungeon, I had no idea how to travel to
            Orgrimmar. When I got to Orgrimmar to meet up with the group, I discovered a couple more transportation
