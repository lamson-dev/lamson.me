            <h1 class="post-title">How to Find and Complete a Quest in World of Warcraft</h1>

            <div class="post-content">
                <p>A quest is a task given to a player character that yields a reward when
                    completed. Rewards can be experience points (XP) or game items. According to WoW Wiki, there are
                    currently more than 9,500 quests in WoW. This instruction will have you to find and complete a
                    quest, also with a few tips and tricks. Depend on the difficulty of the assigned quest, you will
                    need roughly 15 minutes to complete this instruction with a simple low level quest, or up to a
                    couple hours for a very complex one.</p>

                <em>Note: This instruction assumes you already have a character created in World of Warcraft.</em>

                <h4>Step 1: Find the quest givers and talk to them</h4>

                <p>Quests can be received by chatting with non-player characters in WoW. These characters have
                    yellow exclamation marks on top of his/her heads as indicators of quest givers. One easy way to
                    find these quest givers is by referencing the mini-map to look for the quest givers.</p>

                <img src="images/image001.png"/>
                <img src="images/image003.png"/>
                <img src="images/image005.png"/>


                <p>When you meet one of these characters, chat with him/her to receive a quest.</p>


                <h4>Step 2: Accept the quests from the quest givers</h4>

                <p>When you talk to the quest givers, you will be assigned tasks to complete to earn rewards. The
                    rewards can be experience points (XP), game money, and/or game items. Choose to accept the quest
                    by clicking the “Accept” button. Be sure to read the instructions of tasks need to complete
                    before accepting the quest. Now you’re ready to do a quest.</p>

                <img src="images/image007.png"/>


                <h4>Step 3: Go to quest assigned location to complete asked tasks</h4>

                <p>When you accepted to do a quest, you either need to go find another non-player character to talk
                    to, or go to a particular area on the map to complete your tasks. You can reference the mini-map
                    to go to the quest area. It’s also helpful to open the big map (hotkey “M”) to see more details
                    of your quests.</p>

                <img src="images/image009.png"/>
                <br/>
                <img src="images/image011.png"/>

                <h4>Step 4: Return to the quest givers to earn rewards</h4>

                <p>When you completed the assigned tasks in a quest, return to the quest givers and chat with them
                    again to earn your rewards. If you don’t remember who or where the quest givers were, use the
                    map as the reference. At this time, the indicator of quest givers will change to yellow question
                    mark (?). It indicates that you have completed your quest and can talk to quest givers to earn
                    rewards.</p>

                <img src="images/image013.png"/>
                <img src="images/image015.png"/>
                <img src="images/image017.png"/>

                <br/>
                <img src="images/image019.png"/>


