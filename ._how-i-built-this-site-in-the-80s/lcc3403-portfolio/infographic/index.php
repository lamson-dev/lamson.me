        <h1 class="post-title">Infographics – Learn, Create, Share</h1>

        <div class="post-content">
            <p>This week in class we touched on of the most popular forms of social
                content - infographics. I am personally a very big fan of this form for a number of reasons.
                One of them is that infographics are so easy to digest for me. I can probably see an
                infographic once and remember its content for a very long time. Infographics are cool. They
                visually present complex information, data or knowledge quickly and clearly. Some people
                think of it as idea art, where they creatively present their ideas in a way that most people
                can understand.
            </p>

            <p>
                I have always wanted to create an infographic for myself, but with my lack of creative
                skills, it never happens until I got to create one with my group this week. It's actually
                much harder than I thought. I couldn't think of a definition on what we should present to
                the class. Later after receiving some inputs from our group members, we decided to choose
                our definition as 'Fighting as a Blood Elf Hunter'. The reason why we chose this was that
                all of the my group members, yes, all of us have character Blood Elf with class Hunter. We
                recorded our thoughts on a piece of paper, then tried to process and refine the ideas.
                Finally, we gathered all the pieces, put it together and finished our (or my) first
                infographic on the board. We had a picture of a Blood Elf character, some definitions of the
                character and the hunter class, a couple pets pictures, skills with names, and some data on
                the number of Blood Elf characters over all characters, over Horde, etc. I'm not sure if we
                did it the right way. We didn't have enough time to receive feedbacks from the professor.
                But to be honest, I didn't like our infographic that much. I felt like something was missing
                in our infographic, or... a poster to me. I wished we had more time to put in something that
                can show some kind of patterns and trends in it. Oh well, maybe it just me being critical
                and expected something more impressive than what we had.
